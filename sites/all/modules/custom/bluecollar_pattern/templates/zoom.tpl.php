<h1>Zoom In/Out</h1>
<hr>
<p>Users can control their browsers zoom in/zoom out level using +/- keys.</p>

<h3><strong>Not Acceptable - needs to be fixed</strong></h3>

<ul>
    <li>The website <em>layout</em> and <em>grid</em> should not have massive breaks or shifts.</li>
    <li>Key functionality should not break or be unusable.</li>
</ul>
