<h3><strong>Acceptable - Does not need to be fixed, or reported</strong></h3>

<ul>
    <li>Small visual inconsistences are expected and are OK</li>
    <li>Rough edges and artifacts on bitmap images are OK</li>
    <li>Icons and bitmaps that are &quot;fuzzy&quot; are OK</li>
    <li>Minor visual artifacts, leading and bad kerning on fonts are OK</li>
    <li>Small differences in line-heights are OK</li>
</ul>

